import UIKit

class IonIcon {

    static let kFontName = "Ionicons"
    private static let kFontIconSizeOffset: CGFloat = 4

    static func icon(type: IonIconType, ofSize size: CGFloat, color: UIColor = ThemeManager.current.other) -> NSMutableAttributedString {
        return NSMutableAttributedString(
            string: type.rawValue,
            attributes: [
                NSFontAttributeName: UIFont.ionIconFont(ofSize: size + kFontIconSizeOffset),
                NSForegroundColorAttributeName: color
            ])
    }

    static func constraint(label: UILabel, toSize size: CGSize, maxFontSize: CGFloat, minFontSize: CGFloat) -> CGFloat {
        // Set the frame of the label to the targeted rectangle
        let rect = CGRect(x: 0, y: 0, width: size.width, height: size.height)
        label.frame = rect

        // Try all font sizes from largest to smallest font size
        var fontSize = maxFontSize

        // Fit label width wize
        let constraintSize = CGSize(width: label.frame.width, height: CGFloat.greatestFiniteMagnitude)

        repeat {
            // Set current font size
            label.font = UIFont(name: label.font.fontName, size: fontSize)

            // Find label size for current font size
            let textRect = ((label.text ?? "") as NSString).boundingRect(
                with: constraintSize,
                options: [.usesFontLeading],
                attributes: [NSFontAttributeName: label.font],
                context: nil)

            // Done, if created label is within target size
            if textRect.height <= label.frame.size.height {
                break
            }

            // decrease the font size and try again
            fontSize -= 2
        } while fontSize > minFontSize

        return fontSize
    }

    static func image(iconType type: IonIconType, size: CGFloat = 32, color: UIColor) -> UIImage? {
        return UIImage.image(withIconType: type, iconColor: color, andSize: CGSize(width: size, height: size))
    }

}

extension IonIcon {
    static func titleIcon(type: IonIconType, color: UIColor = ThemeManager.current.other) -> NSMutableAttributedString {
        return self.icon(type: type, ofSize: FontManager.currentFont.titleSize, color: color)
    }

    static func textIcon(type: IonIconType, color: UIColor = ThemeManager.current.other) -> NSMutableAttributedString {
        return self.icon(type: type, ofSize: FontManager.currentFont.textSize, color: color)
    }

    static func subtitleIcon(type: IonIconType, color: UIColor = ThemeManager.current.other) -> NSMutableAttributedString {
        return self.icon(type: type, ofSize: FontManager.currentFont.subtitleSize, color: color)
    }
}

extension UIImage {

    static func image(withIconType type: IonIconType, backgroundColor: UIColor = UIColor.clear, iconColor: UIColor = UIColor.white, andSize size: CGSize) -> UIImage? {

        UIGraphicsBeginImageContextWithOptions(size, false, 0)

        var textRect = CGRect.zero
        textRect.size = size

        // rectangle
        let path = UIBezierPath(rect: textRect)
        backgroundColor.setFill()
        path.fill()

        // text
        var fontSize = size.width
        let font = UIFont.ionIconFont(ofSize: fontSize)

        let label = UILabel()
        label.font = font
        label.text = type.rawValue
        fontSize = IonIcon.constraint(
            label: label,
            toSize: size,
            maxFontSize: 50,
            minFontSize: 5)
        iconColor.setFill()

        let style = NSMutableParagraphStyle()
        style.alignment = .center
        (type.rawValue as NSString).draw(
            in: textRect,
            withAttributes: [
                NSFontAttributeName: font,
                NSForegroundColorAttributeName: iconColor,
                NSBackgroundColorAttributeName: backgroundColor,
                NSParagraphStyleAttributeName: style
            ])

        // return image
        let image = UIGraphicsGetImageFromCurrentImageContext()
        UIGraphicsEndImageContext()

        return image
    }

}

extension UIFont {
    static func ionIconFont(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: IonIcon.kFontName, size: size)!
    }
}

